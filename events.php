<div class="panel">
<h2 align="center">GAIL Vadodara & Vaghodia organized Hasya Kavi Sammelan<hr color="blue" style="float:center; width:78%"/></h2>
<p align="justify" style="margin-left:200px; margin-right:200px">GAIL Vadodara & Vaghodia organized Hasya Kavi Sammelan on 26.03.2019 at GAIL Complex, Akota, Vadodara. </p>
<p align="justify" style="margin-left:200px; margin-right:200px">The Chief Guest Shri Mukesh Kumar Tiwari, CGM (O & M) / OIC, GAIL, Vadodara inaugurated the Sammelan by lighting the lamp and Shri S K Mehrotra, General Manager (GPU-O&M), GAIL Vaghodia, invited poets and dignitaries accompanied the chief guest in the lamp lighting. Shri U. S. Baidya, DGM (HR) presented the welcome address. Mrs Rita V Mohile introduced the poets. Invited poets were honored and welcomed by flower bouquet and shawl by the Senior Officials present on the occasion.</p>
<p align="justify" style="margin-left:200px; margin-right:200px">Chief Guest Shri Mukesh Kumar Tiwari addressed the gathering on the occasion where he emphasized on the usage of Hindi as Rajbhasha. He mentioned that GAIL is fully compliance with the official language policy of the Government of India. He welcomed poets by reciting the lines from the poetry of Poet Neeraj - ‘Aatma ke soundarya ka shabd rup hai kavya, manav hona bhagya hai, kavi hona soubhagya’.</p>
<p align="justify" style="margin-left:200px; margin-right:200px">Dr. Kirti Kale, a famous poet from New Delhi, Dr. Praveen Shukla, Dr. Kailash Mandela from Rajasthan, Shri Nandkishor Akela from Madhya Pradesh and Prof. Syed Nadeem Ali, Ghazalkar from Vadodara were present among the invited poets. Smt Mukta Mehrotra, wife of Shri SK Mehrotra, GM (GPU-O&M), GAIL Vaghodia recited her compositions.</p>
<p align="justify" style="margin-left:200px; margin-right:200px">In this Hasya Kavi Sammelan, senior officials of different offices and institutions, Narakas (Undertakings), Vadodara office and Hindi officials and personnel of GAIL, Vadodara, Vaghodia, retired personnel participated along with their families participated. Special guest on this occasion, Assistant Commissioner of Income Tax Department, Shri Pankaj Kumar, Regional Commissioner, P.F. Shri Sanjay Kumar Rai, Mrs. Shubra Agrawal, wife of Shri Sudhir Kumar, ED of Gujarat Refinery and Chief Manager (OL), GAIL Corporate Office, Shri Bhupendra Singh Bhadauria were also present.</p>
